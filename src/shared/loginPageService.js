angular.module("appExemplo").provider("loginPage", function () {
  var _isLoginPage = true;

  this.setIsLoginPage = function (value) {
    _isLoginPage = value;
  };

  this.getIsLoginPage = function () {
    return _isLoginPage;
  };

  this.$get = function () {
    return {
      start: function () {
        return _isLoginPage;
      },
    };
  };
});
